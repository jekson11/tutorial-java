public class ForLoop {
    public static void main(String[] args) {
        for (var perulangan = 1; perulangan <= 10; perulangan++) {
            System.out.println("Perulangan Ke " + perulangan);
        }

        for (var perulangan = 10; perulangan >= 1; perulangan--) {
            System.out.println("Perulangan Ke " + perulangan);
        }

    }
}

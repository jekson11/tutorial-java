public class OperasiPerbandingan {
    public static void main(String[] args) {

        int e = 400;
        int f = 400;

        System.out.println(e < f);
        System.out.println(e > f);
        System.out.println(e <= f);
        System.out.println(e >= f);
        System.out.println(e == f);
        System.out.println(e != f);

    }
}
